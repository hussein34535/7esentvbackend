-- 7. PACKAGES
create table if not exists packages (
  id bigint generated by default as identity primary key,
  name text not null,
  description text,
  price numeric not null default 0,
  duration_days integer not null default 30,
  features jsonb default '[]'::jsonb,
  is_active boolean default true,
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

-- 8. PROMO CODES
create table if not exists promo_codes (
  code text primary key,
  discount_percent integer not null default 0,
  max_uses integer default -1, -- -1 means infinite
  used_count integer default 0,
  expires_at timestamptz,
  is_active boolean default true,
  created_at timestamptz default now()
);

-- 9. PAYMENT METHODS
create table if not exists payment_methods (
  id bigint generated by default as identity primary key,
  name text not null,
  number text, -- e.g. Phone number or Account number
  instructions text,
  image jsonb,
  is_active boolean default true,
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

-- 10. USERS (Subscriptions) - Updated
create table if not exists users (
  id text primary key, -- Firebase UID
  email text not null,
  subscription_end timestamptz,
  plan_id bigint references packages(id),
  status text default 'active', -- active, cancelled, banned
  joined_at timestamptz default now(),
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

