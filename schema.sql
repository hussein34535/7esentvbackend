-- Enable UUID extension if needed (good practice, though we use integers mostly)
create extension if not exists "uuid-ossp";

-- 1. CHANNELS
create table if not exists channels (
  id bigint generated by default as identity primary key,
  name text not null,
  stream_link jsonb,
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

-- 2. CATEGORIES
create table if not exists channel_categories (
  id bigint generated by default as identity primary key,
  name text not null,
  is_premium boolean default false,
  sort_order integer default 0,
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

-- 3. RELATIONS (Channels <-> Categories)
create table if not exists _rel_channels_categories (
  id bigint generated by default as identity primary key,
  channel_id bigint references channels(id) on delete cascade,
  category_id bigint references channel_categories(id) on delete cascade,
  unique (channel_id, category_id)
);

-- 4. MATCHES
create table if not exists matches (
  id bigint generated by default as identity primary key,
  team_a text not null,
  team_b text not null,
  logo_a jsonb,
  logo_b jsonb,
  match_time text not null,
  channel text,
  commentator text,
  champion text,
  stream_link jsonb,
  is_premium boolean default false,
  is_published boolean default true,
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

-- 5. GOALS
create table if not exists goals (
  id bigint generated by default as identity primary key,
  title text,
  image jsonb,
  url jsonb,
  time text,
  is_premium boolean default false,
  is_published boolean default true,
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

-- 6. NEWS
create table if not exists news (
  id bigint generated by default as identity primary key,
  title text,
  image jsonb,
  link jsonb,
  date text,
  is_premium boolean default false,
  is_published boolean default true,
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

